public class Exe004 {
    public static void main(String[] args) {
        /*
         * Número Reverso
         * Problema: Inverta a ordem dos dígitos de um número.
         * Entrada: 1234
         * Saída: 4321
         */
        int[] entrada = {1,2,3,4};
        for (int i = (entrada.length); i>=0;i--) {
            System.out.print(i+ " ");
        }        
    }
}
