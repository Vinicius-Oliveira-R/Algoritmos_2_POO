public class Exe002 {
    /*Média de Três Números
Problema: Escreva um programa/método que receba três números e calcule a média aritmética deles.
Entrada: 4 6 8
Saída: 6 */
    public static void main(String []args){
        int entrada1 = 4;
        int entrada2 = 6;
        int entrada3 = 8;
        int media  = (entrada1+entrada2+entrada3)/3;
        System.out.println("A média de "+entrada1+" + "+entrada2+" + "+entrada3+" é: "+media);
    }

}
